package anhtuannguyen.oop.Object;

public class Object {
    protected float x;
    protected float y;

    public float getx() {
        return x;
    }

    public void setx(float x) {
        this.x = x;
    }

     public float gety() {
        return y;
    }

    public void sety(float y) {
        this.y = y;
    }

}
